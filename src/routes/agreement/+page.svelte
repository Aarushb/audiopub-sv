<h1>The Core Philosophy</h1>
<ul>
  <li>
    This platform is all about open, public sharing. If you upload it, everyone
    can listen to it and download it. It's that simple.
  </li>
  <li>
    I built this because I believe in the power of free and open audio exchange.
    No hidden agendas, no corporate overlords, just pure audio goodness.
  </li>
</ul>

<h1>Your Content, Your Rights</h1>
<ul>
  <li>
    Everything you upload belongs to you. I don't claim any ownership, ever.
  </li>
  <li>
    By uploading, you're agreeing to share your audio with the world. Remember,
    anyone can listen to and download your uploads.
  </li>
  <li>
    I'm not in the business of content creation or rights management. I'm just
    providing the pipes for your audio to flow through.
  </li>
</ul>

<h1>What I Do With Your Audio</h1>
<ul>
  <li>I host it. That's the primary function.</li>
  <li>
    I might transcode it to ensure it plays nicely across different devices and
    browsers.
  </li>
  <li>
    I make it available for streaming and download to anyone who visits the
    site.
  </li>
  <li>That's it. No secret sauce, no hidden processes.</li>
</ul>

<h1>What I Absolutely Don't Do</h1>
<ul>
  <li>I don't sell your content. Ever.</li>
  <li>
    I don't use your audio to train AI models. I wouldn't even know how to begin
    doing that, but even if I learn how, I won't do it.
  </li>
  <li>
    I don't run analytics on your listening habits or upload patterns. I'm a
    developer, not a data scientist.
  </li>
  <li>
    I don't monetize this platform through ads or premium features. It's a labor
    of love, please respect that.
  </li>
</ul>

<h1>The Technical Bits</h1>
<ul>
  <li>
    The site runs on a basic stack: Express, Node.js with typescript, and
    MariaDB. Nothing fancy.
  </li>
  <li>
    Files are stored in block storage in the cloud. I won't name names, but it's
    a big one.
  </li>
  <li>
    I use HTTPS for all connections because security matters, even for a small
    project like this.
  </li>
  <li>
    The code isn't open-source (yet), but I'm considering it for the near
    future.
  </li>
</ul>

<h1>Downloading and Sharing</h1>
<ul>
  <li>
    Every audio file has a download button. Click it, and you've got the file.
    It's that easy.
  </li>
  <li>Please be respectful when sharing. Don't spam or abuse the platform.</li>
</ul>

<h1>Content Guidelines</h1>
<ul>
  <li>
    Don't upload anything illegal. I really shouldn't have to say this, but here
    we are.
  </li>
  <li>Respect copyright. If it's not yours to share, don't share it.</li>
  <li>
    No hate speech, explicit violence, or anything that would make your grandma
    disappointed in you. But if you have the type of grandma who's into that,
    Still don't do it.
  </li>
</ul>

<h1>Takedowns and Removals</h1>
<ul>
  <li>
    If you want your upload gone, just delete it. It'll be wiped from the
    server, no questions asked.
  </li>
  <li>
    If someone claims you've uploaded their content without permission, I'll
    have to take it down while things get sorted. I don't want to, but them's
    the breaks.
  </li>
</ul>

<h1>The Future of the Platform</h1>
<ul>
  <li>
    I'm just one person, so updates might be slow. But I'm committed to keeping
    this thing running.
  </li>
  <li>
    I have no plans to sell out to a big corp or change the core mission of free
    audio sharing.
  </li>
  <li>
    If I ever can't maintain the site anymore, I'll give ample warning so you
    can download your stuff.
  </li>
</ul>

<h1>Privacy and Data</h1>
<ul>
  <li>
    I don't collect personal data beyond what's absolutely necessary (like an
    email for your account).
  </li>
  <li>
    There are no analytics, no tracking, and only the most basic of cookies. I
    mean for now the only thing I can think of is the session cookie.
  </li>
  <li>
    Your listening habits are your business. I don't log them, analyze them, or
    care about them, frankly.
  </li>
</ul>

<h1>Costs and Sustainability</h1>
